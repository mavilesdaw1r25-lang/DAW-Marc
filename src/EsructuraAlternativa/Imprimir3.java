package EsructuraAlternativa;

public class Imprimir3 {

    public static void main(String[] args) {

        //for (cont=valorInicial;condiciio per continuar increment o decrement)
        for (int i = 100; i >= 1; i--) {
            System.out.println(i);
        }
        for (int i = 0; i <= 98; i += 2) {
            System.out.println(i);
        }
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }
    }
}
